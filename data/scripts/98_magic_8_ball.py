import random

ANSWERS = [
    "Бесспорно.",
    "Мне кажется — да.",
    "Пока не ясно, попробуй снова.",
    "Не рассчитывай на это.",
    "Определённо да.",
    "Мой ответ — нет.",
    "Спроси позже.",
    "Знаки говорят — да.",
    "Очень сомнительно.",
    "Вне всяких сомнений.",
]


def main() -> None:
    question = input("Задайте вопрос: ").strip()
    if not question:
        print("Сначала задайте вопрос.")
        return
    print(random.choice(ANSWERS))


if __name__ == "__main__":
    main()
